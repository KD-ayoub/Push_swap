#include "header.h"

int	ft_strncmp(const char *s1, const char *s2, size_t n)
{
	unsigned char	*st1;
	unsigned char	*st2;
	size_t			i;

	st1 = (unsigned char *)s1;
	st2 = (unsigned char *)s2;
	i = 0;
	while ((st1[i] != '\0' || st2[i] != '\0') && (i < n))
	{
		if ((st1[i] < st2[i]) || (st1[i] > st2[i]))
		{
			return (1);
		}
		i++;
	}
	return (0);
}
